------------view of shipment performance
CREATE OR REPLACE VIEW SHIPMENT_PERFORMANCE_VIEW AS
SELECT 
    CATEGORY_NAME,
    SHIPPING_MODE,
    COUNT(*)                                   AS TOTAL_ORDERS,
    SUM(ORDER_ITEM_QUANTITY)                   AS TOTAL_QUANTITY,
    ROUND(SUM(SALES), 2)                       AS TOTAL_REVENUE,
    ROUND(AVG(DAYS_FOR_SHIPPING_REAL), 2)      AS AVG_SHIPPING_DAYS,
    ROUND(AVG(DAYS_FOR_SHIPMENT_SCHEDULED), 2) AS AVG_SCHEDULED_DAYS
FROM RAW_SUPPLY_CHAIN
GROUP BY CATEGORY_NAME, SHIPPING_MODE
ORDER BY TOTAL_REVENUE DESC;


