WITH YEARLY_SALES AS (
    SELECT 
        DEPARTMENT_NAME,
        YEAR(TRY_TO_DATE(ORDER_DATE_DATEORDERS, 'MM/DD/YYYY HH24:MI')) AS SALE_YEAR,
        ROUND(SUM(SALES), 2)             AS TOTAL_SALES
    FROM RAW_SUPPLY_CHAIN
    GROUP BY DEPARTMENT_NAME, YEAR(TRY_TO_DATE(ORDER_DATE_DATEORDERS, 'MM/DD/YYYY HH24:MI'))
)
SELECT 
    CURRENT_YEAR.DEPARTMENT_NAME,
    CURRENT_YEAR.SALE_YEAR,
    CURRENT_YEAR.TOTAL_SALES            AS THIS_YEAR_SALES,
    LAST_YEAR.TOTAL_SALES               AS LAST_YEAR_SALES,
    ROUND(CURRENT_YEAR.TOTAL_SALES 
        - LAST_YEAR.TOTAL_SALES, 2)     AS DIFFERENCE,
    ROUND((CURRENT_YEAR.TOTAL_SALES 
        - LAST_YEAR.TOTAL_SALES) * 100.0 
        / LAST_YEAR.TOTAL_SALES, 2)     AS CHANGE_PERCENTAGE
FROM YEARLY_SALES CURRENT_YEAR
JOIN YEARLY_SALES LAST_YEAR
    ON  CURRENT_YEAR.DEPARTMENT_NAME = LAST_YEAR.DEPARTMENT_NAME
    AND CURRENT_YEAR.SALE_YEAR       = LAST_YEAR.SALE_YEAR + 1
ORDER BY CHANGE_PERCENTAGE DESC;

---------------------history tracking trigger
CREATE OR REPLACE TABLE SHIPMENT_HISTORY (
    HISTORY_ID        NUMBER AUTOINCREMENT PRIMARY KEY,
    ORDER_ID          NUMBER,
    CHANGED_AT        TIMESTAMP DEFAULT CURRENT_TIMESTAMP(),
    CHANGE_TYPE       VARCHAR(10),
    OLD_ORDER_STATUS  VARCHAR(50),
    OLD_SHIPPING_DAYS NUMBER,
    OLD_SALES         FLOAT
);

---------stream
CREATE OR REPLACE STREAM SHIPMENT_STREAM
ON TABLE RAW_SUPPLY_CHAIN;


------------task
CREATE OR REPLACE TASK SHIPMENT_HISTORY_TASK
    WAREHOUSE = COMPUTE_WH
    SCHEDULE  = '1 MINUTE'
WHEN
    SYSTEM$STREAM_HAS_DATA('SHIPMENT_STREAM')
AS
    INSERT INTO SHIPMENT_HISTORY (
        ORDER_ID,
        CHANGE_TYPE,
        OLD_ORDER_STATUS,
        OLD_SHIPPING_DAYS,
        OLD_SALES
    )
    SELECT 
        ORDER_ID,
        CASE 
            WHEN METADATA$ACTION = 'DELETE' THEN 'DELETE'
            ELSE 'UPDATE'
        END,
        ORDER_STATUS,
        DAYS_FOR_SHIPPING_REAL,
        SALES
    FROM SHIPMENT_STREAM
    WHERE METADATA$ACTION = 'DELETE';

    ALTER TASK SHIPMENT_HISTORY_TASK RESUME;
    CALL UPDATE_SHIPMENT(75936, 2, 'LATE');


    SELECT * FROM SHIPMENT_HISTORY;
