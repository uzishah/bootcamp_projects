--Total Quantity Shipped Per Product Category
SELECT 
    CATEGORY_NAME,
    SUM(ORDER_ITEM_QUANTITY) AS TOTAL_QUANTITY_SHIPPED
FROM RAW_SUPPLY_CHAIN
GROUP BY CATEGORY_NAME
ORDER BY TOTAL_QUANTITY_SHIPPED DESC;


----Most Efficient Warehouses by Average Shipping Time

SELECT 
    SHIPPING_MODE,
    ROUND(AVG(DAYS_FOR_SHIPPING_REAL), 2)        AS AVG_ACTUAL_DAYS,
    ROUND(AVG(DAYS_FOR_SHIPMENT_SCHEDULED), 2)   AS AVG_SCHEDULED_DAYS,
    ROUND(AVG(DAYS_FOR_SHIPMENT_SCHEDULED 
              - DAYS_FOR_SHIPPING_REAL), 2)       AS EFFICIENCY_SCORE,
    COUNT(*)                                      AS TOTAL_SHIPMENTS
FROM RAW_SUPPLY_CHAIN
GROUP BY SHIPPING_MODE
ORDER BY EFFICIENCY_SCORE DESC;


----Total Shipment Value Per Supplier

SELECT 
    DEPARTMENT_NAME,
    COUNT(*)                                    AS TOTAL_ORDERS,
    SUM(ORDER_ITEM_QUANTITY)                    AS TOTAL_QUANTITY,
    ROUND(SUM(SALES), 2)                        AS TOTAL_SALES_VALUE,
    ROUND(SUM(ORDER_PROFIT_PER_ORDER), 2)       AS TOTAL_PROFIT,
    ROUND(AVG(SALES), 2)                        AS AVG_ORDER_VALUE
FROM RAW_SUPPLY_CHAIN
GROUP BY DEPARTMENT_NAME
ORDER BY TOTAL_SALES_VALUE DESC;

----Top 5 Products by Highest Shipment Quantity

SELECT 
    PRODUCT_NAME,
    CATEGORY_NAME,
    SUM(ORDER_ITEM_QUANTITY)        AS TOTAL_QUANTITY_SHIPPED,
    COUNT(*)                        AS TOTAL_ORDERS,
    ROUND(SUM(SALES), 2)            AS TOTAL_REVENUE
FROM RAW_SUPPLY_CHAIN
GROUP BY PRODUCT_NAME, CATEGORY_NAME
ORDER BY TOTAL_QUANTITY_SHIPPED DESC
LIMIT 5;




-----------Distribution of Shipment Values Per Product Category

SELECT 
    CATEGORY_NAME,
    COUNT(*)                                AS TOTAL_ORDERS,
    ROUND(SUM(SALES), 2)                    AS TOTAL_VALUE,
    ROUND(AVG(SALES), 2)                    AS AVG_ORDER_VALUE,
    ROUND(MIN(SALES), 2)                    AS MIN_ORDER_VALUE,
    ROUND(MAX(SALES), 2)                    AS MAX_ORDER_VALUE,
    ROUND(STDDEV(SALES), 2)                 AS VALUE_SPREAD,
    ROUND(SUM(SALES) * 100.0 / 
          SUM(SUM(SALES)) OVER (), 2)       AS PERCENTAGE_OF_TOTAL
FROM RAW_SUPPLY_CHAIN
GROUP BY CATEGORY_NAME
ORDER BY TOTAL_VALUE DESC;



----------STORED PROCEDURE ---> UPDATE SHIPMENT STATUS
CREATE OR REPLACE PROCEDURE UPDATE_SHIPMENT(
    P_ORDER_ID     NUMBER,
    P_DELAY_DAYS   NUMBER,
    P_NEW_STATUS   VARCHAR
)
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
BEGIN
    UPDATE RAW_SUPPLY_CHAIN
    SET 
        DAYS_FOR_SHIPPING_REAL = DAYS_FOR_SHIPPING_REAL + :P_DELAY_DAYS,
        ORDER_STATUS           = :P_NEW_STATUS
    WHERE ORDER_ID = :P_ORDER_ID;
    
    RETURN 'SUCCESS: Order updated!';
END;
$$;

